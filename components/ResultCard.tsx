import { useState } from 'react'
import { BeforeAfterSlider } from './BeforeAfterSlider'
import { Download, Edit3, Bookmark } from 'lucide-react'

type ResultCardProps = {
    imageUrl: string
    modelName: string
    originalImageUrl?: string
    onSave?: () => void
    onRevise?: () => void
    onDownload?: () => void
    saving?: boolean
}

export function ResultCard({
    imageUrl,
    modelName,
    originalImageUrl,
    onSave,
    onRevise,
    onDownload,
    saving = false,
}: ResultCardProps) {
    const [showComparison, setShowComparison] = useState(false)

    const handleDownload = async () => {
        if (onDownload) return onDownload()

        try {
            const response = await fetch(imageUrl)
            const blob = await response.blob()
            const url = URL.createObjectURL(blob)
            const a = document.createElement('a')
            a.href = url
            a.download = `BildOro-${modelName.toLowerCase().replace(/\s+/g, '-')}-${Date.now()}.jpg`
            a.click()
            URL.revokeObjectURL(url)
        } catch (error) {
            console.error('Download failed:', error)
        }
    }

    return (
        <div className="border border-border rounded-xl bg-background overflow-hidden shadow-sm hover:shadow-md transition-shadow">
            {/* Header */}
            <div className="flex items-center justify-between p-3 border-b border-border">
                <div className="font-medium text-sm text-text">{modelName}</div>
                {originalImageUrl && (
                    <button
                        onClick={() => setShowComparison(!showComparison)}
                        className="text-xs text-primary hover:underline"
                    >
                        {showComparison ? 'Show Result' : 'Compare'}
                    </button>
                )}
            </div>

            {/* Image / Comparison */}
            <div className="bg-background relative" style={{ aspectRatio: '1 / 1' }}>
                {showComparison && originalImageUrl ? (
                    <BeforeAfterSlider
                        beforeImage={originalImageUrl}
                        afterImage={imageUrl}
                        beforeLabel="Original"
                        afterLabel="Generated"
                    />
                ) : (
                    <img
                        src={imageUrl}
                        alt={`Generated by ${modelName}`}
                        className="object-cover w-full h-full"
                    />
                )}
            </div>

            {/* Minimal action bar */}
            <div className="flex justify-end items-center gap-3 p-3 border-t border-border">
                {onRevise && (
                    <button
                        onClick={onRevise}
                        title="Edit / Revise"
                        className="text-muted-foreground hover:text-primary cursor-pointer transition-colors"
                    >
                        <Edit3 className="size-5" />
                    </button>
                )}
                <button
                    onClick={handleDownload}
                    title="Download"
                    className="text-muted-foreground hover:text-primary cursor-pointer transition-colors"
                >
                    <Download className="size-5" />
                </button>
                {onSave && (
                    <button
                        onClick={onSave}
                        disabled={saving}
                        title={saving ? 'Saving...' : 'Save to Gallery'}
                        className={`text-muted-foreground hover:text-primary cursor-pointer transition-colors ${saving ? 'opacity-50 cursor-not-allowed' : ''
                            }`}
                    >
                        <Bookmark className="size-5" />
                    </button>
                )}
            </div>
        </div>
    )
}
